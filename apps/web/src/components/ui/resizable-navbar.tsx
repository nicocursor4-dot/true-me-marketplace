export const MobileNavMenu = ({
  children,
  className,
  isOpen,
  onClose,
}: MobileNavMenuProps) => {
  return (
    <AnimatePresence>
      {isOpen && (
        <>
          {/* Overlay */}
          <motion.div
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            exit={{ opacity: 0 }}
            className="fixed inset-0 bg-black/50 z-[9998]"
            onClick={onClose}
          />
          
          {/* Full screen menu */}
          <motion.div
            initial={{ x: "100%" }}
            animate={{ x: 0 }}
            exit={{ x: "100%" }}
            transition={{
              type: "spring",
              stiffness: 300,
              damping: 30
            }}
            className={cn(
              "fixed right-0 top-0 h-screen w-screen bg-white z-[9999] flex flex-col",
              className
            )}
          >
            {/* Header with logo only (close button handled by toggle) */}
            <div className="flex items-center justify-between p-4 border-b border-trueme-gold/20">
              <img 
                src="/images/logos/trueme-logo.png" 
                alt="True Me Logo" 
                className="h-12 w-12 object-contain"
              />
            </div>
            
            {/* Menu content */}
            <div className="flex-1 flex flex-col justify-center px-8 space-y-6">
              {children}
            </div>
          </motion.div>
        </>
      )}
    </AnimatePresence>
  );
};

